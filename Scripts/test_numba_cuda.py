from numba import jit
import math

@jit
def hypot(x, y):
	# Implementation from https://en.wikipedia.org/wiki/Hypot
	x = abs(x);
	y = abs(y);
	t = min(x, y);
	x = max(x, y);
	t = t / x;
	return x * math.sqrt(1+t*t)

print(hypot(3.0,4.0))